; PROGRAM TO SEARCH A KEY ELEMENT IN A SORTED ARRAY USING BINARY SEARCH

.MODEL SMALL

.DATA

ARRAY DW 1134H,1567H,2345H

LEN DW ($-ARRAY)/2

KEY EQU 1567H

MSG1 DB "ELEMENT FOUND AT POSITION ",'$'
MSG2 DB "ELEMENT NOT FOUND",'$'

.CODE

MOV AX,@DATA
MOV DS,AX
MOV BX,01
MOV DX,LEN
MOV CX,KEY

AGAIN:
CMP BX,DX
JA FAILURE
MOV AX,DX
ADD AX,BX
SHR AX,01
MOV SI,AX
DEC SI
ADD SI,SI
CMP CX,ARRAY[SI]
JAE BIGGER
DEC AX
MOV DX,AX
JMP AGAIN

BIGGER:
JE SUCCESS
INC AX
MOV BX,AX
JMP AGAIN

SUCCESS:
LEA DX,MSG1
MOV AH,09H
INT 21H

MOV AX,SI
AAM
ADD AX,3030H
MOV BX,AX
MOV DL,BH
MOV AH,02
INT 21H
MOV DL,BL
MOV AH,02
INT 21H

JMP EXIT


FAILURE:
LEA DX,MSG2
JMP DISPLAY

DISPLAY:
MOV AH,09H
INT 21H


EXIT:

MOV AH,4CH
INT 21H
END
