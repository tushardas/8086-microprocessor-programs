;PROGRAM TO SIMULATE A UP-COUNTER(00-99)

.MODEL SMALL

.CODE

MOV AX,@DATA
MOV DS,AX
MOV CL,00H

L:

MOV AL,CL

MOV AH,00H
AAM
ADD AX,3030H
MOV BX,AX

MOV DL,BH
MOV AH,02
INT 21H

MOV DL,BL
MOV AH,02
INT 21H

MOV DL,' '
MOV AH,02
INT 21H

CMP CL,99
JE F

INC CL

JMP L


F:
MOV AH,4CH
INT 21H
END
