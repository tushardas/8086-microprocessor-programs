;PROGRAM TO PRINT THE ASCII VALUE OF THE KEY ENTERED

PRINTSTRING MACRO MSG
MOV AH,09H
MOV DX,OFFSET MSG
INT 21H
ENDM

.MODEL SMALL
.DATA
MSG1 DB 'PRESS ANY KEY',10,13,'$'
NEWLINE DB 10,13,'$'
REDISP DB 4 DUP(0)

.CODE
MOV AX,@DATA
MOV DS,AX
PRINTSTRING MSG1
PRINTSTRING NEWLINE

MOV AH,01H
INT 21H

MOV AH,00H
MOV SI,OFFSET REDISP
CALL HEX2ASC
MOV BH,07
MOV DX,184FH
MOV AX,0600H
INT 10H
MOV AH,02H
MOV BH,00H
MOV DH,0CH
MOV DL,27H
INT 10H
PRINTSTRING REDISP
MOV AH,4CH
INT 21H

HEX2ASC PROC NEAR
MOV CX,00H
MOV BX,0AH

RPT1:
MOV DX,00H
DIV BX
ADD DL,'0'
PUSH DX
INC CX
CMP AX,0AH
JGE RPT1
ADD AL,'0'
MOV [SI],AL

RPT2:
POP AX
INC SI
MOV [SI],AL
LOOP RPT2
INC SI
MOV AL,'$'
MOV [SI],AL
RET
HEX2ASC ENDP
END
