;PROGRAM TO MOVE THE CURSOR TO THE LOCATION SPECIFIED NY THE USER

.MODEL SMALL

.DATA

M1 DB 10,13,'ENTER ROW(00-24):- ','$'
M2 DB 10,13,'ENTER COL(00-79):- ','$'
M3 DB 10,13,'PRESS ANY KEY ','$'
ROW DB 00
COL DB 00

.CODE

MOV AX,@DATA
MOV DS,AX

MOV AH,09
LEA DX,M1
INT 21H

CALL READ
MOV ROW,AL

MOV AH,09
LEA DX,M2
INT 21H

CALL READ
MOV COL,AL

MOV AH,09
LEA DX,M3
INT 21H

MOV AH,02
MOV DH,ROW
MOV DL,COL
INT 10H

MOV AX,0600H
MOV BH,07
MOV CX,00
MOV DX,184FH
INT 10H


MOV AH,01
INT 21H

MOV AH,4CH
INT 21H


READ PROC
MOV AH,01
INT 21H
AND AL,0FH
MOV BL,AL

MOV AH,01
INT 21H
AND AL,0FH
MOV AH,BL
AAD
RET
READ ENDP
end
