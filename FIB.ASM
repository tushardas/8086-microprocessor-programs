;PROGRAM TO GENERATE FIBONACCI SEQUENCE

.MODEL SMALL

.DATA

FIB DB 10 DUP(0)
N DW 07

.CODE

MOV AX,@DATA
MOV DS,AX
MOV SI,00H
MOV FIB[SI],05H
INC SI
MOV FIB[SI],07H
MOV CX,N
DEC CX
DEC CX
MOV AL,FIB[SI]
BACK:
ADD AL,FIB[SI-1]+0
INC SI
MOV FIB[SI],AL
LOOP BACK

MOV SI,00H
MOV CX,N
L:
MOV AL,FIB[SI]
AAM
ADD AX,3030H
MOV BX,AX
MOV DL,BH
MOV AH,02
INT 21H
MOV DL,BL
MOV AH,02
INT 21H
MOV DL,' '
MOV AH,02
INT 21H
INC SI
LOOP L


MOV AH,4CH
INT 21H
END
