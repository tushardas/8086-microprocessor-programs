;PROGRAM TO RETREIVE CURRENT TIME FROM THE SYSTEM AND DISPLAY IT

.MODEL SMALL

.DATA

MSG DB 10,13,"CURRENT TIME IS $"

.CODE

MOV AX,@DATA
MOV DS,AX

LEA DX,MSG
MOV AH,09H
INT 21H

MOV AH,2CH
INT 21H

MOV AL,CH

CALL DISP
MOV DL,':'
MOV AH,2
INT 21H
MOV AL,CL
CALL DISP
MOV DL,':'
MOV AH,02
INT 21H
MOV AL,DH
CALL DISP
MOV AH,4CH
INT 21H

DISP PROC NEAR
AAM
ADD AX,3030H
MOV BX,AX
MOV DL,BH
MOV AH,2
INT 21H
MOV DL,BL
MOV AH,02
INT 21H
RET
DISP ENDP
MOV AH,4CH
INT 21H
END
