;PROGRAM TO READ THE NAME FROM USER AND DISPLAY IT AT THE MIDDLE OF SCREEN

.MODEL SMALL

.DATA
M1 DB "ENTER NAME",0AH,'$'
M2 DB "WHAT IS YOUR NAME ? ",'$'
NAME1 DB 80 DUP(0)

.CODE
MOV AX,@DATA
MOV DS,AX
MOV SI,OFFSET NAME1
LEA DX,M1
MOV AH,09H
INT 21H

LOC:
MOV AH,01H
INT 21H
CMP AL,0DH
JZ NEXT
MOV [SI],AL
INC SI
JMP LOC

NEXT:
MOV AL,'$'
MOV [SI],AL
MOV BH,07
MOV CX,00
MOV DX,184FH
MOV AX,0600H
INT 10H

MOV AH,02H
MOV BH,00H
MOV DH,12
MOV DL,26
INT 10H

LEA DX,M2
MOV AH,09H
INT 21H
LEA DX,NAME1
MOV AH,09H
INT 21H
MOV AH,4CH
INT 21H
END
